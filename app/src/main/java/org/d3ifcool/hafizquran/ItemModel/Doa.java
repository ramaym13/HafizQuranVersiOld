package org.d3ifcool.hafizquran.ItemModel;

public class Doa {
    private String nmr_doa;

    public String getNmr_doa() {
        return nmr_doa;
    }

    public void setNmr_doa(String nmr_doa) {
        this.nmr_doa = nmr_doa;
    }

    private String judul_doa;
    private String doa;
    private String latin;
    private String arti;

    public String getJudul_doa() {
        return judul_doa;
    }

    public void setJudul_doa(String judul_doa) {
        this.judul_doa = judul_doa;
    }

    public String getDoa() {
        return doa;
    }

    public void setDoa(String doa) {
        this.doa = doa;
    }

    public String getLatin() {
        return latin;
    }

    public void setLatin(String latin) {
        this.latin = latin;
    }

    public String getArti() {
        return arti;
    }

    public void setArti(String arti) {
        this.arti = arti;
    }
}
